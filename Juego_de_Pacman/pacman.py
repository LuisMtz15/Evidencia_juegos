# Función para dibujar un cuadrado en la posición (x, y)
def square(x, y):
    """Dibuja un cuadrado en la posición (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# Función para obtener el desplazamiento de un punto en el tablero
def offset(point):
    """Devuelve el desplazamiento de un punto en los azulejos."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

# Función para verificar si un punto es válido en el tablero
def valid(point):
    """Devuelve True si el punto es válido en los azulejos."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

# Función para dibujar el mundo
def world():
    """Dibuja el mundo utilizando el camino."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

# Función para mover a Pacman y a los fantasmas
def move():
    """Mueve a Pacman y a todos los fantasmas."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 50)

# Función para cambiar la dirección de Pacman
def change(x, y):
    """Cambia la dirección de Pacman si es válida."""
    global last_direction
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y
        last_direction = vector(x, y)

# Función para mover a los fantasmas inteligentemente hacia el pacman dependiendo de sus movimientos
def move_ghosts():
    """Mueve a los fantasmas hacia Pacman de manera más inteligente."""
    for point, course in ghosts:
        diff = pacman - point

        if last_direction == vector(5, 0) and diff.x > 0:
            course.x = 5
            course.y = 0
        elif last_direction == vector(-5, 0) and diff.x < 0:
            course.x = -5
            course.y = 0
        elif last_direction == vector(0, 5) and diff.y > 0:
            course.x = 0
            course.y = 5
        elif last_direction == vector(0, -5) and diff.y < 0:
            course.x = 0
            course.y = -5
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        if valid(point + course):
            point.move(course)

    ontimer(move_ghosts, 10)


